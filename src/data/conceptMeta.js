/**
 * "Gunun kavrami" (Têgeha Rojê) — daily concept selector.
 * Uses day-of-year to deterministically select a concept index.
 */
export function getDailyConceptIndex(totalConcepts) {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now - start) / 86400000);
  return dayOfYear % totalConcepts;
}
